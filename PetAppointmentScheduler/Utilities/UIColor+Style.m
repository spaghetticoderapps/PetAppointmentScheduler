@implementation UIColor (Style)

+ (UIColor *) darkGreenColor {
    return [UIColor colorWithRed:0.40 green:0.70 blue:0.40 alpha:1.00];
}

+ (UIColor *) paleLightBlueColor {
    return [UIColor colorWithRed:0.91 green:0.93 blue:1.00 alpha:1.00];
}

+ (UIColor *) lightBlueColor {
    return [UIColor colorWithRed:0.13 green:0.42 blue:0.73 alpha:1.00];
}

+ (UIColor *) lightYellowColor {
    return [UIColor colorWithRed:1.00 green:0.97 blue:0.78 alpha:1.00];
}

@end
